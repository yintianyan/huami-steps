/**
 * lib/calendar.js — 节假日感知
 *
 * 中国法定节假日（2025-2026），基于国务院发布
 * 节假日当天按周末模式处理
 */

// 格式：'YYYY-MM-DD'
const HOLIDAYS_2025 = [
  '2025-01-01', // 元旦
  '2025-01-28', '2025-01-29', '2025-01-30', '2025-01-31', // 春节
  '2025-02-01', '2025-02-02', '2025-02-03', '2025-02-04',
  '2025-04-04', '2025-04-05', '2025-04-06', // 清明
  '2025-05-01', '2025-05-02', '2025-05-03', '2025-05-04', '2025-05-05', // 劳动节
  '2025-05-31', '2025-06-01', '2025-06-02', // 端午
  '2025-10-01', '2025-10-02', '2025-10-03', '2025-10-04', // 国庆+中秋
  '2025-10-05', '2025-10-06', '2025-10-07', '2025-10-08',
];

const HOLIDAYS_2026 = [
  '2026-01-01', '2026-01-02', '2026-01-03', // 元旦
  '2026-02-17', '2026-02-18', '2026-02-19', '2026-02-20', // 春节
  '2026-02-21', '2026-02-22', '2026-02-23',
  '2026-04-05', '2026-04-06', '2026-04-07', // 清明（预估）
  '2026-05-01', '2026-05-02', '2026-05-03', '2026-05-04', '2026-05-05', // 劳动节
  '2026-06-19', '2026-06-20', '2026-06-21', // 端午（预估）
  '2026-09-25', '2026-09-26', '2026-09-27', // 中秋（预估）
  '2026-10-01', '2026-10-02', '2026-10-03', '2026-10-04', // 国庆
  '2026-10-05', '2026-10-06', '2026-10-07', '2026-10-08',
];

const ALL_HOLIDAYS = new Set([...HOLIDAYS_2025, ...HOLIDAYS_2026]);

// 调休工作日（周末但需上班）
const WORKDAYS = new Set([
  '2025-01-26', '2025-02-08', // 春节调休
  '2025-04-27', // 劳动节调休
  '2025-09-28', '2025-10-11', // 国庆调休
  '2026-02-14', // 春节调休
  '2026-04-11', '2026-05-09', '2026-10-10', // 清明/劳动/国庆调休（预估）
]);

function isHoliday(today) {
  return ALL_HOLIDAYS.has(today);
}

/** 调休工作日（周末但上班） */
function isWorkday(today) {
  return WORKDAYS.has(today);
}

function isRestDay(today) {
  const { isWeekend } = require('./time');
  if (isWorkday(today)) return false; // 调休日按工作日
  return isWeekend(today) || isHoliday(today);
}

module.exports = { isHoliday, isRestDay, isWorkday };
